public class Integral {
    private int rowNum;
    private int commId;
    private String commName;
    private double integralMoney;
    private int integralNumber;
    private String picture;
    private String httpPicture;

    public int getRowNum() {
        return rowNum;
    }

    public void setRowNum(int rowNum) {
        this.rowNum = rowNum;
    }

    public int getCommId() {
        return commId;
    }

    public void setCommId(int commId) {
        this.commId = commId;
    }

    public String getCommName() {
        return commName;
    }

    public void setCommName(String commName) {
        this.commName = commName;
    }

    public double getIntegralMoney() {
        return integralMoney;
    }

    public void setIntegralMoney(double integralMoney) {
        this.integralMoney = integralMoney;
    }

    public int getIntegralNumber() {
        return integralNumber;
    }

    public void setIntegralNumber(int integralNumber) {
        this.integralNumber = integralNumber;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getHttpPicture() {
        return httpPicture;
    }

    public void setHttpPicture(String httpPicture) {
        this.httpPicture = httpPicture;
    }
}
