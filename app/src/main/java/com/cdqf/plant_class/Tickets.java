package com.cdqf.plant_class;

public class Tickets {
    private String AdmissionTicketPic;
    private String AdmissionTicketTitle;
    private double AdmissionTicketPrice;
    private int AdmissionTicketCount;
    private double AdmissionTicketTotalCount;
    private String AdmissionTicketCode;

    public String getAdmissionTicketPic() {
        return AdmissionTicketPic;
    }

    public void setAdmissionTicketPic(String admissionTicketPic) {
        AdmissionTicketPic = admissionTicketPic;
    }

    public String getAdmissionTicketTitle() {
        return AdmissionTicketTitle;
    }

    public void setAdmissionTicketTitle(String admissionTicketTitle) {
        AdmissionTicketTitle = admissionTicketTitle;
    }

    public double getAdmissionTicketPrice() {
        return AdmissionTicketPrice;
    }

    public void setAdmissionTicketPrice(double admissionTicketPrice) {
        AdmissionTicketPrice = admissionTicketPrice;
    }

    public int getAdmissionTicketCount() {
        return AdmissionTicketCount;
    }

    public void setAdmissionTicketCount(int admissionTicketCount) {
        AdmissionTicketCount = admissionTicketCount;
    }

    public double getAdmissionTicketTotalCount() {
        return AdmissionTicketTotalCount;
    }

    public void setAdmissionTicketTotalCount(double admissionTicketTotalCount) {
        AdmissionTicketTotalCount = admissionTicketTotalCount;
    }

    public String getAdmissionTicketCode() {
        return AdmissionTicketCode;
    }

    public void setAdmissionTicketCode(String admissionTicketCode) {
        AdmissionTicketCode = admissionTicketCode;
    }
}
